package Bit_Manipulation;

public class BitWiseOperators {
    public static void main(String[] args) {
       int a = 1;
       int b = 2;
        System.out.println(Integer.toBinaryString(a));
        System.out.println(Integer.toBinaryString(b));
        System.out.println(a & b);
        System.out.println(a | b);
        System.out.println(a ^ b);
    }
}